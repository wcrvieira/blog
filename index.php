            <h2>Trojan</h2>
            <img src="images/trojan.png" alt="Cavalo de Tróia">
            <p> Na informática, um cavalo de tróia (trojan horse) é um programa, normalmente recebido como um "presente"
                (por exemplo, cartão virtual, álbum de fotos, protetor de tela, jogo, etc), que além de executar funções 
                para as quais foi aparentemente projetado, também executa outras funções normalmente maliciosas e sem o 
                conhecimento do usuário.
            </p>
